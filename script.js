function toggleFeatures(btn){

let box = btn.nextElementSibling;

if(box.style.display==="block"){
box.style.display="none";
btn.innerText="View Features";
}else{
box.style.display="block";
btn.innerText="Hide Features";
}

}

function renderSection(id, filterFn, limit=6){
const el = document.getElementById(id);
if(!el) return;

products
.filter(filterFn)
.sort((a,b)=>((b.oldPrice-b.price)-(a.oldPrice-a.price)))
.slice(0,limit)
.forEach(p=>{

let discount = Math.round(((p.oldPrice-p.price)/p.oldPrice)*100);

el.innerHTML += `
<div class="card">

<div class="badge">🔥 ${discount}% OFF</div>

<img src="${p.image}">
<h3>${p.title}</h3>

<div class="price">
₹${p.price}
<span class="old-price">₹${p.oldPrice}</span>
</div>



<a href="${p.link}" target="_blank">
<button class="buy-btn">🛒 Check Deal</button>
</a>

</div>
`;
});
}

